public class WorkoutType {

    private String type;
    private int reps;
    private int sets;

    public WorkoutType()
    {

    }
    public void setType(String t)
    {
        type = t;
    }

    public void setReps(int r)
    {
        reps = r;
    }

    public void setSets(int s)
    {
        sets = s;
    }
}
